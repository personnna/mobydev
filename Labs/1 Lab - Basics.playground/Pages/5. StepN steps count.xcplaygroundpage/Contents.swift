/*:
## App Exercise - StepN steps count
 
Create a variable called `steps` that will keep track of the number of steps user took throughout the day. Set its initial value to 0 to represent the step count first thing in the morning. Print `steps` to the console.
 */
/*:
 Создайте переменную под названием `steps`, которая будет отслеживать количество шагов, пройденных пользователем в течение дня. Установите его начальное значение на 0, чтобы представить количество шагов первым делом с утра. Выведите `steps` в консоль.
 */
var steps = 0
print(steps)

/*:  Now assume the tracker has been keeping track of steps all morning, and you want to show the user the latest step count. Update `steps` to be 2000. Print `steps` to the console. Then print "Good job! You're well on your way to your daily goal.
 */
/*: Теперь предположим, что трекер отслеживал шаги все утро, и вы хотите показать пользователю последнее количество шагов. Обновите `steps` до 2000. Выведите `steps` в консоль. Затем напечатайте «Хорошая работа! Вы уже на пути к своей ежедневной цели».
 */
steps = 2000
print(steps)
print("Good job! You're well on your way to your daily goal.")
