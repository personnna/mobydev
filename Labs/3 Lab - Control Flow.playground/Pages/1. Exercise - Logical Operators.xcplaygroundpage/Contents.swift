/*:
## Exercise - Logical Operators

 For each of the logical expressions below, print out what you think the resulting value will be (`true` or `false`). Then print out the actual expression to see if you were right. An example has been provided below.
 
    43 == 53
    print(false)
    print(43 == 53)
 ##
 
 Для каждого из приведенных ниже логических выражений выведите то, каким, по вашему мнению, будет результирующее значение («истина» или «ложь»). Затем распечатайте фактическое выражение, чтобы увидеть, были ли вы правы. Выше приведен пример.
 
 */

print(9 == 9)                           // Expected: true
print(9 != 9)                           // Expected: false
print(47 > 90)                          // Expected: false
print(47 < 90)                          // Expected: true
print(4 <= 4)                           // Expected: true
print(4 >= 5)                           // Expected: false
print((47 > 90) && (47 < 90))           // Expected: false
print((47 > 90) || (47 < 90))           // Expected: true
print(!true)                            // Expected: false



