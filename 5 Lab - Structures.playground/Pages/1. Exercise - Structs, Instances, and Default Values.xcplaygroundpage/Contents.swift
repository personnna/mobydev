## Exercise - Structs, Instances, and Default Values
 
 Imagine you are creating an app that will monitor location. Create a `GPS` struct with two variable properties, `latitude` and `longitude`, both with default values of 0.0.
 */
/*:
Представьте, что вы создаете приложение, которое будет отслеживать местоположение. Создайте структуру `GPS` с двумя переменными свойствами, `широта` и `долгота`, оба со значениями по умолчанию 0,0.
 */
